CREATE TABLE IF NOT EXISTS topo.region (
  simulation_uuid uuid NOT NULL,
  node_id integer NOT NULL,
  region_code integer NOT NULL,

  PRIMARY KEY (simulation_uuid, node_id),

  FOREIGN KEY (simulation_uuid, node_id)
    REFERENCES topo.node(simulation_uuid, node_id)
    ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS topo.vsat (
  simulation_uuid uuid NOT NULL,
  node_id integer NOT NULL,
  region_code integer,
  la double precision NOT NULL,
  lo double precision NOT NULL,
  alt double precision NOT NULL,

  PRIMARY KEY (simulation_uuid, node_id),

  FOREIGN KEY (simulation_uuid, node_id)
    REFERENCES topo.node(simulation_uuid, node_id)
    ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS topo.ray (
  simulation_uuid uuid NOT NULL,
  node_id integer NOT NULL,
  origin_node_id integer NOT NULL,
  ray_kind text NOT NULL,
  cluster_id integer NOT NULL,
  frequency_id integer NOT NULL,
  aperture double precision NOT NULL DEFAULT 0,
  connect_limit integer,

  PRIMARY KEY (simulation_uuid, node_id),

  FOREIGN KEY (simulation_uuid, node_id)
    REFERENCES topo.node(simulation_uuid, node_id)
    ON DELETE CASCADE,

  FOREIGN KEY (simulation_uuid, origin_node_id)
    REFERENCES topo.node(simulation_uuid, node_id)
    ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS topo.topology_snapshot (
  simulation_uuid uuid NOT NULL
    REFERENCES topo.simulation(simulation_uuid) ON DELETE CASCADE,

  time_index integer NOT NULL,
  snapshot jsonb NOT NULL DEFAULT '{}'::jsonb,

  PRIMARY KEY (simulation_uuid, time_index),

  FOREIGN KEY (simulation_uuid, time_index)
    REFERENCES topo.simulation_time(simulation_uuid, time_index)
    ON DELETE CASCADE,

  CHECK (time_index >= 0)
);

CREATE INDEX IF NOT EXISTS ix_topology_snapshot_time
  ON topo.topology_snapshot(simulation_uuid, time_index);


